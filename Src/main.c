/**Count 60 seconds for MQ-3 to heat up
*/
void count_time_for_heat_up()
{
	 if(timer<=600)
		 timer++;
	 else
		 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_RESET);
}
/**Show current value of var alko on screen and refresh screen
*/
void show_alko(){
	 if(initialized==1){
		 if(!interrupt_init){
			 ssd1331_display_num(33, 22, temp_alko, 4, FONT_1608, BLACK);
		 	 temp_alko = alko;
		 	 ssd1331_display_num(33, 22, temp_alko, 4, FONT_1608, color);
		 }
	 }
}

/**Timer interrupt handle
 * @param htim: pointer to an timer instance
*/
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	 if(htim->Instance == TIM10){
		 show_alko();
		 count_time_for_heat_up();
		 check_max();
	 }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if(GPIO_Pin == Button_Pin && interrupt_init == 0)
	{
		interrupt_init = 1;
	}
}
